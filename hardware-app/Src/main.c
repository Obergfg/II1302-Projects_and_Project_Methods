   /*
    * Receives the digital data received from the light and moisture sensors of the STM32 module
		* and sends them to the controller.
    *
		* @hadc is a pointer to the analog to digital handler.
    *
    */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef * hadc){
	
	if (__HAL_ADC_GET_FLAG(hadc, ADC_FLAG_EOC)){
		ADC_raw[ADC_i] = HAL_ADC_GetValue(hadc);
		ADC_i++;
	}
	
	if(__HAL_ADC_GET_FLAG(hadc, ADC_FLAG_EOS))
		ADC_i = 0;	
}

/*
	Updates the ADC_raw variable to contain the current values gathered from the sensors.
*/
unsigned int *updateSensorValues(){
	HAL_ADC_Start_IT(&hadc);
	return ADC_raw;
}

